func TestHeaderMap(t *testing.T) {
	block0 := NewBlockWithHeader(&Header{
		Extra:    []byte("test Block"),
		GasLimit: 10,
		Time:     10,
		Height:   10,
	})
	block1 := NewBlockWithHeader(&Header{
		Extra:    []byte("test Block"),
		GasLimit: 20,
		Time:     20,
		Height:   20,
	})
	block2 := NewBlockWithHeader(&Header{
		Extra:    []byte("test Block"),
		GasLimit: 30,
		Time:     30,
		Height:   30,
	})
	hm := HeaderMap{}.Add(block0.Header()).Add(block1.Header()).Add(block2.Header())
	// AvgGasLimit()
	res := hm.AvgGasLimit()
	exp := uint64(20)
	if res != exp {
		t.Fatalf("AvgGasLimit failed, got %v != %v", res, exp)
	}
	// GetMaxTime()
	res = hm.GetMaxTime()
	exp = uint64(30)
	if res != exp {
		t.Fatalf("GetMaxTime failed, got %v != %v", res, exp)
	}
	// GetMaxHeight()
	res1 := hm.GetMaxHeight()
	exp1 := uint64(30)
	if res1 != exp1 {
		t.Fatalf("GetMaxHeight failed, got %v != %v", res, exp)
	}
	// GetMinHeight()
	res1 = hm.GetMinHeight()
	exp1 = uint64(10)
	if res1 != exp1 {
		t.Fatalf("GetMinHeight failed, got %v != %v", res, exp)
	}
}
